#include <stdio.h>
#include <stdlib.h>

//Estudo de estrutura repetição em C, contadores de pares no intervalo entre 150 e 200.

int main(int argc, char *argv[]) {
	
	int i;
	int cont = 0;

	for(i = 150; i <= 200; i = i + 2){
		cont++;
		
	
		
	}
	
	printf("\n Existem %d numeros pares no intervalo de 150 a 200\n", cont);
	
	
	return 0;	
}

